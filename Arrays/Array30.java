package Array;
// For each loop in Array
// for each loop is only used for display the values of arrays because it will deals with the only 
// array values as a input in k
public class Array30 {
    public static void main(String[] args) {
        int[] arr = {10,20,40,75,11};
        for(int k:arr){
            System.out.print(k + " ");
        }
    }
}
